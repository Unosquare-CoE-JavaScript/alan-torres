
// Destructuring: Easily extract array elements or object properties and store them in variables


// Array Destructuring
const [a, b] = ['Hello', 'Max'];
console.log(a); // Hello
console.log(b); // Max

// Object Destructuring
const { name } = { name: 'Max', age: '28' };
console.log(name); // Max
console.log(age); // undefined
